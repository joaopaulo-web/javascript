let num1 = prompt('Digite um Número: ')
let num1Convertido = parseInt(num1)
let num2 = prompt('Digite um Segundo Número: ')
let num2Convertido = parseInt(num2)


if (num1Convertido > num2Convertido){
    alert(`O maior número digitado foi ${num1Convertido}`)
}else{
    alert(`O maior número digitado foi ${num2Convertido}`)
}